    def save_dictionary(self):
        """Сохранение словаря в файл"""
        try:
            data = {
                'frequencies': dict(self.word_frequency)
            }
            with open(self.dictionary_path, 'w', encoding='utf-8') as f:
                json.dump(data, f, ensure_ascii=False, indent=2)
            self.logger.debug("Словарь сохранен")
        except Exception as e:
            self.logger.error(f"Ошибка сохранения словаря: {e}")

    def predict(self, prefix: str, max_suggestions: int = 5) -> List[str]:
        """Предсказание слов по префиксу"""
        if not prefix or len(prefix) < 1:
            return []

        # Приводим к нижнему регистру
        prefix_lower = prefix.lower()

        # Ищем слова, начинающиеся с префикса
        candidates = []
        for word, frequency in self.word_frequency.items():
            if word.startswith(prefix_lower):
                candidates.append((word, frequency))

        # Сортируем по частоте (более частые - выше) и по длине (короткие выше)
        candidates.sort(key=lambda x: (-x[1], len(x[0])))

        # Берем первые max_suggestions слов
        suggestions = [word for word, _ in candidates[:max_suggestions]]

        if suggestions:
            self.logger.debug(f"T9 для '{prefix}': {suggestions}")
        else:
            self.logger.debug(f"T9 для '{prefix}': нет совпадений")

        return suggestions

    def add_word(self, word: str):
        """Добавление нового слова в словарь"""
        word_lower = word.lower()

        if word_lower in self.word_frequency:
            self.word_frequency[word_lower] += 1
        else:
            self.word_frequency[word_lower] = 1

        self.save_dictionary()
        self.logger.info(f"Добавлено слово: {word}")

    def update_frequency(self, word: str):
        """Обновление частоты использования слова"""
        word_lower = word.lower()
        self.word_frequency[word_lower] += 1
        self.save_dictionary()

    def get_word_frequency(self, word: str) -> int:
        """Получение частоты использования слова"""
        return self.word_frequency.get(word.lower(), 0)